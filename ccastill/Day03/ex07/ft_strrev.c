char	*ft_sttrev(char *str)
{
	int		n;
	char	m;

	n = 0;
	while (str[n] != '\0')
	{
		n++;
	}
	n--;
	m = 0;
	while (n >= m)
	{
		c = str[m];
		*(str + m) = *(str + n);
		*(str + n) = c;
		n--;
		m++;
	}
	return (str);
}
