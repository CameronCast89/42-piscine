#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int		pos;

	pos = 0;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = malloc(sizeof(int) * (max - min));
	while (pos < max - min)
	{
		(*range)[pos] = min + pos;
		pos++;
	}
	return (sizeof(range));
}
