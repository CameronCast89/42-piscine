int		ft_str_is_alpha(char *str)
{
	int		pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if ((str[pos] >= 'A' && str[pos] <= 'Z')
			|| (str[pos] >= 'a' && str[pos] <= 'z'))
			pos++;
		else
			return (0);
	}
	return (1);
}
