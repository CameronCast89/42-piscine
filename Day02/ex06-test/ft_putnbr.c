void	ft_putnbr(int nb)
{
	int num;
	int num_2;

	num = nb;
	num_2 = nb;
	num_2 = num % 10;
	num = num / 10;
	if (num > 0)
	{
		ft_putnbr(num);
	}
	ft_putchar(num_2 + '0');
}
