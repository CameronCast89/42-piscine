char	*ft_strupcase(char *str)
{
	int		pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if ('a' <= str[pos] && str[pos] <= 'z')
		{
			str[pos] = str[pos] - 32;
		}
		pos++;
	}
	return (str);
}
