#include <stdlib.h>

int		*ft_map(int *tab, int length, int (*f)(int))
{
	int		*array;
	int		i;

	i = 0;
	array = (int*)malloc(sizeof(int) * length);
	while (i < length)
	{
		array[i] = f(tab[i]);
		i++;
	}
	return (array);
}
