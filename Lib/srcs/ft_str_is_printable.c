int		ft_str_is_printable(char *str)
{
	int		pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if (str[pos] >= 32 && str[pos] < 127)
			pos++;
		else
			return (0);
	}
	return (1);
}
