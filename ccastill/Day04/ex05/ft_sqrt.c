int		ft_sqrt(int nb)
{
	int		num;

	num = 1;
	if (nb == 1)
		return (1);
	if (nb == 0)
		return (0);
	while (num <= nb / 2 && num > 0)
	{
		if (nb == num * num)
		{
			return (num);
		}
		num++;
	}
	return (0);
}
